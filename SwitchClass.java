package com.aktar.switching;

public class SwitchClass {

	public static void main(String[] args) {
		int tuna;
		tuna = 4;
		switch (tuna) {
		case 1:
			System.out.println("Drop in the sea");
			break;
		case 2:
			System.out.println("take in the boat");
			break;
		case 3:
			System.out.println("cook it for Lunch");
		case 4:
			System.out.println("I donot know what to do");
			break;
			

		}

	}

}
